    private void cargaDatosP() {

        jtpagos.setDefaultRenderer(Object.class, new Render());
        JButton btn1 = new JButton("Detalle");
        // Datos
        String dni = txtdni.getText();

        try {
            // Proceso
            PagosController control = new PagosController();
            lista = control.consultaxPagodni(dni);
            if (lista.isEmpty()) {
                throw new Exception("Error");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        // Acceso al objeto Table
        DefaultTableModel tabla;
        tabla = (DefaultTableModel) jtpagos.getModel();

        // Eliminar  todas las filas
        tabla.setRowCount(0);

        // Cargar Datos
        for (Pagos men : lista) {
            Object[] rowData = {
                men.getIdPago(),
                men.getIdMembresia(),
                men.getIdSocio(),
                men.getApellido(),
                men.getNombre(),
                men.getDNI(),
                men.getTipo_de_Pago(),
                men.getIGV(),
                men.getSubtotal(),
                men.getTotal(),
                men.getNroCuotas(),
                men.getEstado(),
                btn1
            };
            tabla.addRow(rowData);
        }

    }

