    private void jtpagosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtpagosMouseClicked
        int column = jtpagos.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / jtpagos.getRowHeight();
        if (row < jtpagos.getRowCount() && row >= 0 && column < jtpagos.getColumnCount() && column >= 0) {
            Object value = jtpagos.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                //                JButton boton = (JButton) value;

                int rows = jtpagos.getSelectedRow();
                if (rows == -1) {
                    return;
                }

                DetallePagos view;
                view = new DetallePagos(new JFrame(), true);
                view.setRowData(lista.get(row));
                view.setLocationRelativeTo(null);
                view.setVisible(true);

            }
        }
    }//GEN-LAST:event_jtpagosMouseClicked

