    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        PromocionesController controlpromo = new PromocionesController();
        PlanesController controlplan = new PlanesController();
        PagosController controlpago = new PagosController();
        DetPagosController controldetpagos= new DetPagosController();
        MembreController controlmembresia = new MembreController();
        try {
            plan = controlplan.leerxid(idplan);
            promo = controlpromo.leerxid(idpromo);
            if (cbotipopago.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un tipo de pago");
                return;
            }
            if (cbonumcuotas.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un número de cuota");
                return;
            }
            pago.setIdSocio(jtxtidsocio.getText());
            String tipopago = cbotipopago.getSelectedItem().toString();
            pago.setTipo_de_Pago(tipopago);
            int nrocuotas = Integer.parseInt(cbonumcuotas.getSelectedItem().toString());
            pago.setNroCuotas(nrocuotas);
            double total=plan.getImporte()-(plan.getImporte()*promo.getDescuento());
            double subtotal=total/1.18;
            double igv=total-subtotal;
            
            pago.setTotal(total);
            pago.setSubtotal(subtotal);
            pago.setIGV(igv);
            pago.setEstado("Pendiente");
            String fecha=ObtenerFecha(FechaActual());
            listafechas=fechasdepago(fecha, nrocuotas);
            try {
                controlpago.crear(pago);
                
                for (int i = 0; i < listafechas.size(); i++) {
                detpagos.setIdPago(pago.getIdPago());
                detpagos.setIdEmpleado(jlblnomemp.getText());
                detpagos.setNroCuota(i+1);
                detpagos.setImporte(total/nrocuotas);
                detpagos.setF_UPago(listafechas.get(i).toString());
                detpagos.setEstado("Pendiente");
                controldetpagos.crear(detpagos);
                
                controlmembresia.insertaPago(idmem, pago.getIdPago());
            }
                
            } catch (Exception e) {
                throw new Exception("Error."+e.getMessage());
            }

            JOptionPane.showMessageDialog(null, "Pago  generado Correctamente, su id es: "
                    + pago.getIdPago());


        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error. " + e.getMessage());
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

