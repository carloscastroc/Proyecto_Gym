    public void setRowData(String id) {

        Membresia membresia = new Membresia();
        MembreController controlm = new MembreController();
        membresia = controlm.leerxid(id);
        idmem=membresia.getIdMembresia();
        jtxtidsocio.setText(membresia.getIdSocio());
        idplan = membresia.getIdPlan();
        idpromo = membresia.getIdPromociones();
        PromocionesController control = new PromocionesController();
        promo = control.leerxid(idpromo);
        if (!"P0001".equals(promo.getIdPromociones())) {
            cbonumcuotas.setSelectedIndex(1);
            cbonumcuotas.setEnabled(false);
        }

    }

