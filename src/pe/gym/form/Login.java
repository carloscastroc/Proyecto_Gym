    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {

            // Datos
            String usuario = this.jtxtuser.getText();
            String clave = String.valueOf(this.jtxtpass.getPassword());

            // Proceso
            EmpleadoController control = new EmpleadoController();
            control.validarUsuario(usuario, clave);
            Empleado bean = (Empleado) Session.get("empleado");

            // Cargar formulario principal
            JOptionPane.showMessageDialog(null, "Bienvenido Usuario: " + bean.getNombre(),"Mensaje de Bienvenida", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            switch (bean.getIdCargo()) {
                case "C0001":
                    MainAdministrador.main(null);
                    break;
                case "C0002":
                    MainAsistente.main(null);
                    break;
                case "C0003":
                    MainCajero.main(null);
                    break;
                case "C0004":
                    MainNutricionista.main(null);
                    break;
                case "C0005":
                    MainEvaluador.main(null);
                    break;
                default:
                    throw new IllegalArgumentException();
            }
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Error. " + e.getMessage());
            
        }
    }//GEN-LAST:event_btnLoginActionPerformed

